package poly.edu.junitdemo.lab3;

public class NumberUtils {
    /**
     * Kiểm tra một số có phải là số nguyên dương hay không
     * @param number Số cần kiểm tra
     * @return true nếu là số nguyên dương, ngược lại trả về false
     */
    public static boolean isPositiveNumber(int number) {
        return number > 0;
    }
}
